package com.example.recyclerview;

public class Elearning {

    private String nama;
    private String jumlah;


    public Elearning(String nama, String jumlah) {
        this.nama = nama;
        this.jumlah = jumlah;


    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String npm) {
        this.jumlah = jumlah;
    }


}